package Java;

public class Variable {
    public static void main(String[] args) {
        // Print value to console 
        System.out.println("Hello World");

        //// Data Types 
        // byte, short, int, long, float, double, char, boolean
        // Can't use keyword as variable name 

        // 1 byte = 8 bits

        // int take 4 bytes
        int age = 23; 

        // long take 8 bytes
        // in last we use l for declaring long variable
        long id = 123l;

        // char take 2 bytes
        char c = 'a';	

        // short take 2 bytes
        short s = 12;

        //float takes 4 bytes
        float f = 2.34f;

        // double takes 8 bytes
        double d = 2.34d;

        // boolean is undefined 
        boolean b = true;
    }
}
